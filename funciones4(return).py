def suma(num1,num2):     #la función recibirá dos parámetros o argumentos (puede recibir varios)             
	
	resultado=num1+num2
	return resultado	  #esto se realiza para asignar en una variable lo que devuelva la función


almacena=suma(3,2)        #guarda valor en esta variable para después utilizarla
print(almacena)

